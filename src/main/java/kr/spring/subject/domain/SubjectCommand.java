package kr.spring.subject.domain;

public class SubjectCommand {
	private int s_num;
	private String lv1;
	private String lv2;
	
	public int getS_num() {
		return s_num;
	}
	public void setS_num(int s_num) {
		this.s_num = s_num;
	}
	public String getLv1() {
		return lv1;
	}
	public void setLv1(String lv1) {
		this.lv1 = lv1;
	}
	public String getLv2() {
		return lv2;
	}
	public void setLv2(String lv2) {
		this.lv2 = lv2;
	}
	
}
